CREATE TABLE EMPLOYEE(
	EMP_ID INT NOT NULL PRIMARY KEY ,
	EMP_NAME VARCHAR(25),
	SALARY INT,
	EMP_DEPT VARCHAR(25));

	 CREATE TABLE MANAGER(
	MGR_NAME VARCHAR(25),
	EMP_ID INT NOT NULL PRIMARY KEY,
	MGR_ID INT
	FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE(EMP_ID)ON DELETE CASCADE);
 
 CREATE TABLE TIMESHEET(
         EMP_ID INT, 
         DATE DATETIME,
         ACTIVITY VARCHAR(25),
         HOURS_SPENT INT,
         STATUS VARCHAR(25),
         FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE(EMP_ID)ON DELETE CASCADE);

